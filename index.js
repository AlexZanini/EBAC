"use strict";
// Função de multiplicação
function multiplicar(num1, num2) {
    return num1 * num2;
}
// Função de saudação
function saudar(nome) {
    return `Olá ${nome}`;
}
// Exemplos de uso
let resultado = multiplicar(3, 4); // Retorna 12
console.log(resultado);
let saudacao = saudar("Alex Zanini"); // Retorna "Olá Alex Zanini"
console.log(saudacao);
